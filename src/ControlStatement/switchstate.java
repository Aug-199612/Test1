package ControlStatement;

public class switchstate {

	public static void main(String[] args) {
		char i='@';
		switch(i)
		{
		case '!':
			System.out.println("Tomorrow is the monday");
			break;
		case '@':
			System.out.println("Tomorrow is the tuesday");
			break;
		case '#':
			System.out.println("Tomorrow is the wednesday");
			break;
		case '$':
			System.out.println("Tomorrow is the thursday");
			break;
		case '%':
			System.out.println("Tomorrow is the friday");
			break;
		case '&':
			System.out.println("Tomorrow is the saturday");
			break;
		case '*':
			System.out.println("Tomorrow is the sunday");
			break;
			default:
				System.out.println("wrong charachter insert");
		}
		
	}

}
