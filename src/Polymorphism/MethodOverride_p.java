//2) Run time polymorphism
//In Runtime Polymorphism, method declaration is going to get binded to its definition at
//Runtime/execution time, based on object creation is known as runtime Polymorphism.
//As binding takes during Runtime/execution time, so it is also known as late binding.
////once binding is done, again rebinding can be done, so it is called dynamic binding.
//Method overriding is an example of Runtime Polymorphism.
public class MethodOverride_p {
	
		public void print()
		{
		System.out.println("Print method of Parent");
		}

}
